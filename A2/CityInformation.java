package cs486.artificial.inteligence;

/**
 * data class for storing information about a city
 */
public class CityInformation {
    public String city;
    public int x;
    public int y;

    public CityInformation(String city, int x, int y) {
        this.city = city;
        this.x = x;
        this.y = y;
    }

}
